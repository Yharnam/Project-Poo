    private void rSButtonHover28ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rSButtonHover28ActionPerformed

        b = doublevalidat(this.txt_casilla.getText());
        double suma;
        double resta;
        double multiplicacion;
        double division;
        calcu calcu = new calcu(a, b);
        calcu.suma();
        calcu.resta();
        calcu.multiplicacion();
        calcu.division();
        
        suma = calcu.getSuma();
        resta = calcu.getResta();
        multiplicacion = calcu.getMultiplicacion();
        division = calcu.getDivision();
        
        try{
        
        switch(this.operador){

            case "+": 
                this.txt_casilla.setText(sincero(suma));
                break;
            case "-":
                this.txt_casilla.setText(sincero(resta));
                break;  
            case "*":
                this.txt_casilla.setText(sincero(multiplicacion));
                break;
            case "/": 
                if (b == 0) {
                this.txt_casilla.setText("No se divide entre '0'");
            }else{
                
                this.txt_casilla.setText(sincero(division));
                break;
                    
            }          
        }
            
        }catch(Exception e){
            
        }   
        
    }//GEN-LAST:event_rSButtonHover28ActionPerformed

    public String sincero(double resultado){
        String retorno = "";
        retorno = Double.toString(resultado);
        if (resultado%1 == 0) {
            retorno = retorno.substring(0, retorno.length()-2);
        }
        return retorno;
    }
    
