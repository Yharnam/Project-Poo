    public class calcu implements Calcu{

        
        private double primerNumero;
        private double segundoNumero;
        private double suma;
        private double resta;
        private double multiplicacion;
        private double division;
        
        public calcu(double primerNumero, double segundoNumero) {
        this.primerNumero = primerNumero;
        this.segundoNumero = segundoNumero;
        }
        
        public double getPrimerNumero() {
            return primerNumero;
        }

        public void setPrimerNumero(double primerNumero) {
            this.primerNumero = primerNumero;
        }

        public double getSegundoNumero() {
            return segundoNumero;
        }

        public void setSegundoNumero(double segundoNumero) {
            this.segundoNumero = segundoNumero;
        }

        public double getSuma() {
            return suma;
        }

        public double getResta() {
            return resta;
        }


        public double getMultiplicacion() {
            return multiplicacion;
        }

        public double getDivision() {
            return division;
        }
        
        
        
        @Override
        public void suma() {
            suma = primerNumero + segundoNumero;
        }

        @Override
        public void resta() {
            resta = primerNumero - segundoNumero;
        }

        @Override
        public void multiplicacion() {
            multiplicacion = primerNumero * segundoNumero;
        }

        @Override
        public void division() {
            division = primerNumero / segundoNumero;
        }
        
        
    }
    
    
